import sys
input=lambda:sys.stdin.readline().rstrip()
# 시간의 범위(1~1000)와 편의상 여유를 둔 배열로, 모든 시간에 필요한 버킷 수를 누적 계산하기 위해 초기화
time = [0 for _ in range(1002)]  # 크기를 1002로 설정하여 인덱스 1000까지 포함하고, 범위를 초과하는 t+1도 처리 가능
# 젖소의 수만큼 입력을 처리
for _ in range(int(input())):  # 첫 번째 입력: 젖소의 수 N
    s, t, b = map(int, input().split())  # 각 젖소의 시작 시간(s), 종료 시간(t), 필요한 버킷 수(b)
    # 시작 시간에 b개의 버킷 추가
    time[s] += b  # s 시간에 b개의 버킷 사용 시작  
    # 종료 시간 다음 시간에 b개의 버킷 제거 (t+1에 -b 적용)
    time[t + 1] += -b  # t+1 시간부터는 b개의 버킷 사용 종료
# 누적합을 계산하여 각 시간대에 필요한 버킷 수를 계산
for i in range(1, 1002):  # 시간 범위(1~1001)까지 누적합 계산
    time[i] += time[i - 1]  # 이전 시간의 버킷 수를 누적하여 현재 시간의 총 버킷 수 계산
# 필요한 버킷 수의 최대값 출력
print(max(time))  # 모든 시간대 중 최대 버킷 수를 출력